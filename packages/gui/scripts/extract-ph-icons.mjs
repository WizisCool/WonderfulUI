/**
 * Extract the Phosphor icons used by WonderfulUI into a small offline bundle.
 * Usage: node scripts/extract-ph-icons.mjs
 */
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { icons as ph } from '@iconify-json/ph';

const used = [
  'x',
  'database',
  'bug',
  'info',
  'arrows-clockwise',
  'file-text',
  'folder-open',
  'crosshair',
  'skull',
  'hand-heart',
  'trend-up',
  'star',
  'circle-notch',
  'lightning',
  'play',
  'pause',
  'arrow-counter-clockwise',
  'warning',
  'film-strip',
  'caret-left',
  'caret-right',
  'caret-down',
  'calendar',
  'check',
  'pencil-simple',
  'dots-six-vertical',
  'gear',
  'video',
  'sliders-horizontal',
  'speaker-x',
  'speaker-low',
  'speaker-high',
  'share-network',
  'share',
  'arrows-in',
  'arrows-out',
  'monitor-play',
  'copy',
  'crown',
  'medal',
  'house',
  'magnifying-glass',
  'funnel',
  'download-simple',
  'upload-simple',
  'link',
  'qr-code',
  'wifi-high',
  'device-mobile',
];

const icons = {};
const missing = [];
for (const name of used) {
  if (ph.icons[name]) icons[name] = ph.icons[name];
  else missing.push(name);
}
if (missing.length) {
  console.error('Missing icons:', missing.join(', '));
  process.exit(1);
}

const collection = {
  prefix: 'ph',
  width: ph.width ?? 256,
  height: ph.height ?? 256,
  icons,
};

const root = dirname(fileURLToPath(import.meta.url));
const out = join(root, '..', 'src', 'icons', 'ph-local.ts');
const body = `/**
 * Offline Phosphor subset for WonderfulUI.
 * Generated by scripts/extract-ph-icons.mjs — re-run when adding new ph: icons.
 * Registered at app startup so WebView works without api.iconify.design.
 */
import type { IconifyJSON } from '@iconify/types';

export const phLocal: IconifyJSON = ${JSON.stringify(collection, null, 2)} as IconifyJSON;
`;

writeFileSync(out, body);
console.log(`Wrote ${Object.keys(icons).length} icons → ${out}`);
